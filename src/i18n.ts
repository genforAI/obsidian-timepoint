export type TranslationKey = keyof typeof EN;
export type SupportedLocale = "en" | "zh-CN";

export function resolveLocale(candidate?: string): SupportedLocale {
  const language =
    candidate ??
    (typeof document !== "undefined" ? document.documentElement.lang : "") ??
    (typeof navigator !== "undefined" ? navigator.language : "");
  return language.toLowerCase().startsWith("zh") ? "zh-CN" : "en";
}

export function getLocale(): SupportedLocale {
  return resolveLocale();
}

export function t(
  key: TranslationKey,
  variables: Record<string, string | number> = {},
  locale = getLocale(),
): string {
  const template = (locale === "zh-CN" ? ZH : EN)[key] ?? EN[key];
  return Object.entries(variables).reduce(
    (result, [name, value]) => result.replaceAll(`{${name}}`, String(value)),
    template,
  );
}

export function translationKeys(): TranslationKey[] {
  return Object.keys(EN) as TranslationKey[];
}

export function hasTranslation(key: TranslationKey, locale: SupportedLocale): boolean {
  return Boolean((locale === "zh-CN" ? ZH : EN)[key]);
}
